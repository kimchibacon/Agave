#include "Agave/Core/Application.h"
#include "Agave/Core/Log.h"
#include "Agave/Events/ApplicationEvent.h"

namespace Agave {
    ///=========================================================================
    ///=========================================================================
    Application::Application()
        : m_running(true)
    {
        m_window = std::unique_ptr<Window>(Window::Create());
    }

    ///=========================================================================
    ///=========================================================================
    Application::~Application()
    {}

    ///=========================================================================
    ///=========================================================================
    void Application::Run()
    {
        while (m_running)
        {
            m_window->OnUpdate();
        }
    }
}